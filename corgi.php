    <h2>History of the Breed</h2>
    <p>
        Unlike some dog breeds, the Pembroke Welsh Corgi does not have a traceable breed
history. Its origins are obscured by tales and folklore and even contain ties to the wee folk of
the British Isles.</p>

<p>According to legend, two children tending their family&#39;s cattle on royal lands found a pair of
puppies, which they thought were foxes. When they brought the puppies home, they were
told the dogs were a gift from the fairies. Welsh legends tell us that the fairies would use the
little dogs to pull their carriages or as mounts for them to ride into battle. If you look, you can
still see the marks of the fairy saddle on their shoulders (especially pronounced in the sable
color). As the little puppies that the children brought home grew, they learned to help their
humans watch over their cattle, a task to become a responsibility for their descendants for
the centuries to follow.</p>

<p>The more commonly accepted theory traces back to Scandinavian raiders bringing their
dogs with them to the British Isles, possibly as far back as the 9th or 10th century. The
Swedish Vallhund is seen to bear many similarities to today&#39;s Pembroke Welsh Corgi and is
presumed to have been bred with native Welsh dogs. Any of the offspring that expressed
cattle herding/driving traits were no doubt selectively bred to enhance that skill. It is also
thought that the dogs brought over with Flemish weavers, who settled in Pembrokeshire,
South Wales in the 12th century, were bred with the local cattle dogs adding the Spitz
characteristics that the Pembroke Welsh Corgi expresses today.</p>

<p>The name of the breed is as difficult to nail down as is its origin. One school combines the
Welsh word &quot;cor,&quot; which means &quot;to watch over or gather&quot; with &quot;gi,&quot; a form of the Welsh
word for dog. This was certainly a responsibility of these small cattle herders and
homestead guardians. Another ascribes the word corgi as the Celtic word for dog and that
the Norman invaders thereafter referred to any local dog as a &quot;cur&quot; or mongrel. Finally,
legend pops up again with the interpretation that the word &quot;cor&quot; means &quot;dwarf.&quot; Combine
that with the Welsh form for dog &quot;gi&quot; and you have &quot;dog of the dwarfs or &quot;dwarf dog.&quot; For
many years Corgis (both breeds) were referred to as either &quot;Ci-llathed&quot; meaning &quot;yard long
dog&quot; (we&#39;re talking a Welsh yard here) or as &quot;Ci Sawdlo&quot; due to its nature of nipping at
cattle&#39;s heels.
    </p>
    
    <h2>Appearance</h2>
    <figure>
            <img src="images/corgi.jpg" alt="Cute Corgi">
            <figcaption>Beutiful cute corgi jumping picture</figcaption> 
        </figure>
    <p>
        The Pembroke Welsh Corgi sheds extensively, often daily, with coat &quot;blow outs&quot; often
twice a year. The Pembroke Welsh Corgi has erect ears which are in proportion to the
equilateral triangle of the head. The ears should also be firm, medium in size, and
tapered slightly to a rounded point. The head should be &quot;foxy&quot; in shape and
appearance. Pembroke Welsh Corgis differ from the Cardigan Welsh Corgi by being
shorter in length, having smaller ears, and being slightly straighter of leg. The Pembroke
Welsh Corgi has a &quot;fairy saddle&quot;, somewhat lighter markings on each side of the withers
caused by changes in the thickness, length, and direction of hair growth. The Pembroke
Welsh Corgi sheds mostly in the spring and autumn and may shed annually, with intact
females shedding during heat.</p>

<p>Breed faults exist; some which may acknowledge genetic health conditions such as
&quot;fluffies&quot; which are corgis with very long coats, and &quot;bluies,&quot; which have a dilute colour
(red coats present with a bluish cast).</p>

<p>Some Pembroke Welsh Corgis are born with their tail naturally short or missing. Others
may have their tails docked between 2–5 days old due to historical tradition or in order
to conform to the Breed Standard. Artificial docking was needed for the dog to do its job
as a herding dog in the United Kingdom. A non-herding, &quot;companion&quot; dog was
considered a luxury under tax law, and attracted a tax, so to demonstrate that their dogs
were herding dogs, owners had to ensure the dogs had docked or bobbed tails. The
Kennel Club, the United Kennel Club, and the FCI allow intact tails in Conformation
shows. The AKC Standard states tails should be docked no longer than 2 inches (5 cm).
In many countries, docking has been deemed illegal.</p>
    
